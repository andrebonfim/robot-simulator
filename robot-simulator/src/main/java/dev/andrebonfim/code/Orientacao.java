package dev.andrebonfim.code;

/**
 * Enumeração que define as possíveis orientações de um robô em um ambiente
 * bidimensional.
 * Esta enumeração é usada para especificar a direção em que um robô está
 * orientado ou se move.
 *
 * @author André Luis Bonfim
 * @version 4.0
 */
public enum Orientacao {
  FRENTE, // Representa a orientação para frente
  ATRAS, // Representa a orientação para trás
  ESQUERDA, // Representa a orientação para esquerda
  DIREITA; // Representa a orientação para direita
}
